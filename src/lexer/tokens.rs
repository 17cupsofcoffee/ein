#[derive(Debug, PartialEq)]
pub enum Token<'input> {
    // Sigils
    OpenBrace,
    CloseBrace,
    OpenBracket,
    CloseBracket,
    OpenParen,
    CloseParen,
    Semicolon,
    Comma,
    Dot,
    Plus,
    Minus,
    Star,
    Slash,
    Not,
    NotEqual,
    Equal,
    EqualEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,
    AmpAmp,
    PipePipe,

    // Literals
    Identifier(&'input str),
    String(&'input str),
    Number(f64),

    // Keywords
    Else,
    False,
    Fn,
    For,
    If,
    Nil,
    Print,
    Return,
    This,
    True,
    Let,
    While,
}
